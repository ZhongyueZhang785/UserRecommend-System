    public int[][] qrMap;
    public int[] payloadBitArray;

    public QRMap(String text, PairQR[] zigzagPair, int[] logisticBitArray) {

        PayloadArray payloadArray = new PayloadArray(text);
        payloadBitArray = payloadArray.getBitArray();

        if (payloadBitArray.length == 232) {
            qrMap = arrayVersion1;
        } else {
            qrMap = arrayVersion2;
        }

        for (int i = 0; i < zigzagPair.length; i++) {
            this.setValue(zigzagPair[i], payloadBitArray[i]);
        }

        this.XORLogistic(logisticBitArray);
    }

    public int[][] getQRMap() {
        return qrMap;
    }

    public void setValue(PairQR pair, int val) {
        if (val == 1) {
            qrMap[pair.getRow()][pair.getCol()] = val;
        }
    }

    public void XORLogistic(int[] logisticBitArray) {
        int count = 0;
        for (int row = 0; row < qrMap.length; row++) {
            for (int col = 0; col < qrMap[0].length; col++) {
                qrMap[row][col] = qrMap[row][col] ^ logisticBitArray[count];
                count++;
            }
        }
    }

    public String convertHex() {
        StringBuilder hexQR = new StringBuilder();
        int count = 0;
        int byteInt = 0;
        for (int row = 0; row < qrMap.length; row++) {
            for (int col = 0; col < qrMap[0].length; col++) {
                byteInt = (byteInt * 2) + qrMap[row][col];
                count++;
                if (count == 32) {
                    hexQR.append("0x");
                    hexQR.append(Integer.toHexString(byteInt));
                    count = 0;
                    byteInt = 0;
                }
            }
        }
        if (count != 0) {
            hexQR.append("0x");
            hexQR.append(Integer.toHexString(byteInt));
        }
        String hexQRString = hexQR.toString();
        return hexQRString;
    }

